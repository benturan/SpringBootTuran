package com.bilgeadam.SpringBootTuran.dto;

public class DistrictDto {
	

	private int id;		
	private String	ilce;
	private String	semt;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getIlce() {
		return ilce;
	}
	public void setIlce(String ilce) {
		this.ilce = ilce;
	}
	public String getSemt() {
		return semt;
	}
	public void setSemt(String semt) {
		this.semt = semt;
	}

}
